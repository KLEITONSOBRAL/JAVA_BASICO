/*
Faça um programa em Java que exiba na tela todos os números inteiros de 150 até 300, um por linha.
*/

package com.mycompany.exercicio_13_kleiton;

public class EXERCICIO_13_KLEITON {
    
    public static void main(String[] args) {
        
        for (int i = 150; i <= 300; i++) {
            System.out.println(i);
        }
    }
}
